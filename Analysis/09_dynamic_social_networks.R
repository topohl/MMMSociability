            mean_degree = mean(igraph::degree(g), na.rm = TRUE),
            mean_strength = mean(igraph::strength(g, weights = igraph::E(g)$Weight), na.rm = TRUE),
            clustering = suppressWarnings(igraph::transitivity(g, type = "average", isolates = "zero")),
            components = comps$no,
            largest_component_fraction = max(comps$csize, na.rm = TRUE) / igraph::vcount(g),
            fragmentation_index = 1 - max(comps$csize, na.rm = TRUE) / igraph::vcount(g)
