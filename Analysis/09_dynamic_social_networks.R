  time_col <- first_existing_col(dyad_raw, c("TimeIndex", "HalfHourElapsed", "HalfHourWithinCC0", "HalfHour", "Time", "TimeBin", "datetime", "DateTime"), TRUE, "dyad time column")
  prox_col <- first_existing_col(dyad_raw, c("Weight", "ProximityFraction", "Proximity", "proximity", "contact_fraction", "same_position_fraction", "Distance", "distance", "Interaction", "interaction_weight"), TRUE, "dyad proximity column")
