  phase_col <- first_existing_col(dyad_raw, c("Phase", "phase", "LightDark", "DayNight"), FALSE, "dyad phase column")
  cage_col <- first_existing_col(dyad_raw, c("CageChange", "CC", "CageChangeNum", "Regrouping", "Batch", "Cage"), FALSE, "dyad cage-change column")
  batch_col <- first_existing_col(dyad_raw, c("Batch", "batch"), FALSE, "dyad batch column")
  system_col <- first_existing_col(dyad_raw, c("System", "system", "Cage", "cage"), FALSE, "dyad system column")
  observed_col <- first_existing_col(dyad_raw, c("observation_seconds", "ObservationSeconds", "duration", "DurationSec"), FALSE, "dyad observation seconds column")

  dyad <- dyad_raw %>%
    transmute(
      Focal = as.character(.data[[focal_col]]),
      Partner = as.character(.data[[partner_col]]),
      TimeIndex = .data[[time_col]],
      Weight = suppressWarnings(as.numeric(.data[[prox_col]])),
      Group = if (!is.na(group_col)) as.factor(.data[[group_col]]) else factor("All"),
      Sex = if (!is.na(sex_col)) as.factor(.data[[sex_col]]) else factor("All"),
      Phase = if (!is.na(phase_col)) as.factor(.data[[phase_col]]) else factor("All"),
      CageChange = if (!is.na(cage_col)) as.factor(.data[[cage_col]]) else factor("All"),
      Batch = if (!is.na(batch_col)) as.factor(.data[[batch_col]]) else factor("All"),
      System = if (!is.na(system_col)) as.factor(.data[[system_col]]) else factor("All"),
      observation_seconds = if (!is.na(observed_col)) suppressWarnings(as.numeric(.data[[observed_col]])) else NA_real_,
      BinLevel = bin_level,
      DyadWeightInput = prox_col
    ) %>%
    filter(!is.na(Focal), !is.na(Partner), Focal != Partner, is.finite(Weight)) %>%
    mutate(
      Group = factor(as.character(Group), levels = unique(c(group_levels, sort(unique(as.character(Group)))))),
      CageChangeIndex = parse_cage_change_index(CageChange)
    )

  dyad_qc_tbl <- tibble(
    BinLevel = bin_level,
    DyadWeightInput = prox_col,
    n_rows_raw = nrow(dyad_raw),
    n_rows_valid = nrow(dyad),
    n_focal_animals = n_distinct(dyad$Focal),
    n_partner_animals = n_distinct(dyad$Partner),
    n_time_bins = n_distinct(dyad$TimeIndex),
    use_positive_dyad_weight = use_positive_dyad_weight
  )

  write_table(dyad_qc_tbl, file.path(output_dir, "tables", "dyadic_input_qc.csv"))

  if (nrow(dyad) == 0) {
    warning("Dyadic file was found, but no valid dyadic rows remained after filtering.")
  } else {
    threshold <- if (use_positive_dyad_weight) {
      0
    } else if (proximity_is_distance) {
      quantile(dyad$Weight, probs = 1 - contact_quantile, na.rm = TRUE)
    } else {
      quantile(dyad$Weight, probs = contact_quantile, na.rm = TRUE)
    }

    dyadic_threshold_tbl <- tibble(
      BinLevel = bin_level,
      DyadWeightInput = prox_col,
      ProximityIsDistance = proximity_is_distance,
      ContactQuantile = contact_quantile,
      UsePositiveDyadWeight = use_positive_dyad_weight,
      ContactThreshold = as.numeric(threshold)
    )
    write_table(dyadic_threshold_tbl, file.path(output_dir, "tables", "dyadic_contact_threshold.csv"))

    edge_tbl <- dyad %>%
      mutate(
        Contact = if (use_positive_dyad_weight) {
          Weight > 0
        } else if (proximity_is_distance) {
          Weight <= threshold
        } else {
          Weight >= threshold
        }
      )

    write_table(edge_tbl, file.path(output_dir, "tables", "dyadic_edges_network_input.csv"))

    dyadic_threshold_sensitivity <- map_dfr(threshold_sensitivity_grid, function(q) {
      thr <- if (use_positive_dyad_weight) {
        0
      } else if (proximity_is_distance) {
        quantile(dyad$Weight, probs = 1 - q, na.rm = TRUE)
      } else {
        quantile(dyad$Weight, probs = q, na.rm = TRUE)
      }
      dyad %>%
        mutate(Contact = if (use_positive_dyad_weight) Weight > 0 else if (proximity_is_distance) Weight <= thr else Weight >= thr) %>%
        group_by(BinLevel, DyadWeightInput, ContactQuantile = q, Group, Sex, Phase, CageChange, CageChangeIndex, Batch, System, Focal) %>%
        arrange(TimeIndex, Partner, .by_group = TRUE) %>%
        summarise(
          n_partners = n_distinct(Partner[Contact %in% TRUE]),
          partner_entropy = {
            partner_counts <- table(Partner[Contact %in% TRUE])
            p <- as.numeric(partner_counts) / sum(partner_counts)
            if (length(p) == 0 || !is.finite(sum(p))) NA_real_ else -sum(p * log2(p))
          },
          social_contact_fraction = mean(Contact, na.rm = TRUE),
          .groups = "drop"
        )
    })
    write_table(dyadic_threshold_sensitivity, file.path(output_dir, "tables", "dyadic_partner_threshold_sensitivity.csv"))

    dyad_summary <- edge_tbl %>%
      group_by(BinLevel, DyadWeightInput, Group, Sex, Phase, CageChange, CageChangeIndex, Batch, System, Focal, Partner) %>%
      summarise(
        mean_weight = mean(Weight, na.rm = TRUE),
        median_weight = median(Weight, na.rm = TRUE),
        max_weight = max(Weight, na.rm = TRUE),
        weight_rmssd = calc_rmssd(Weight),
        contact_bin_fraction = mean(Contact, na.rm = TRUE),
        total_observation_seconds = sum(observation_seconds, na.rm = TRUE),
        total_observation_hours = total_observation_seconds / 3600,
        contact_bins = sum(Contact, na.rm = TRUE),
        contact_bins_per_hour = if_else(is.finite(total_observation_hours) & total_observation_hours > 0,
                                        contact_bins / total_observation_hours, NA_real_),
        n_bins = n(),
        .groups = "drop"
      )

