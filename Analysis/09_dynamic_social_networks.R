        n_partners_observed = sum(mean_weight > 0, na.rm = TRUE),
        mean_partner_weight = mean(mean_weight, na.rm = TRUE),
        max_partner_weight = max(mean_weight, na.rm = TRUE),
        top_partner_share = max(partner_weight_share, na.rm = TRUE),
        partner_entropy = -sum(partner_weight_share * log(partner_weight_share + 1e-9), na.rm = TRUE),
