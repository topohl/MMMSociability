          x = NULL,
          y = NULL
        ) +
        scale_fill_manual(values = group_colors, drop = FALSE) +
        make_publication_theme(base_size = 6)

      p_preference <- add_pairwise_brackets(p_preference, preference_brackets, text_size = 1.45)

      save_plot_svg_pdf(p_preference, file.path(output_dir, "figures", "dyadic_partner_preference_summary"), width = 200, height = 135)
    } else {
      message("Package igraph not installed. Dyadic table detected, but true graph metrics skipped.")
    }
  }
} else {
